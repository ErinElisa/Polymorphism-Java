public class Matematika {
    // Tambah
    void pertambahan(int a, int b) {
        int hasil = a + b;
        System.out.println("hasil tambah: " + hasil);
    }

    void pertambahan(double a, double b){
        double hasil = a + b;
        System.out.println("Hasil tambah: " + hasil);
    }

    void pertambahan(int a, int b, int c){
        int hasil = a + b + c;
        System.out.println("Hasil tambah: " + hasil);
    }

    void pertambahan(double a, double b, double c){
        double hasil = a + b + c;
        System.out.println("Hasil tambah: " + hasil);
    }


    // Kurang
    void pengurangan(int a, int b) {
        int hasil = a - b;
        System.out.println("hasil kurang: " + hasil);
    }

    void pengurangan(double a, double b){
        double hasil = a - b;
        System.out.println("Hasil kurang: " + hasil);
    }

    void pengurangan(int a, int b, int c){
        int hasil = a - b - c;
        System.out.println("Hasil kurang: " + hasil);
    }

    void pengurangan(double a, double b, double c){
        double hasil = a - b - c;
        System.out.println("Hasil kurang: " + hasil);
    }


    // Kali
    void perkalian(int a, int b) {
        int hasil = a * b;
        System.out.println("hasil kali: " + hasil);
    }

    void perkalian(double a, double b){
        double hasil = a * b;
        System.out.println("Hasil kali: " + hasil);
    }

    void perkalian(int a, int b, int c){
        int hasil = a * b * c;
        System.out.println("Hasil kali: " + hasil);
    }

    void perkalian(double a, double b, double c){
        double hasil = a * b * c;
        System.out.println("Hasil kali: " + hasil);
    }


    // Bagi
    void pembagian(int a, int b) {
        int hasil = a / b;
        System.out.println("hasil bagi: " + hasil);
    }

    void pembagian(double a, double b){
        double hasil = a / b;
        System.out.println("Hasil bagi: " + hasil);
    }

    void pembagian(int a, int b, int c){
        int hasil = a / b / c;
        System.out.println("Hasil bagi: " + hasil);
    }

    void pembagian(double a, double b, double c){
        double hasil = a / b / c;
        System.out.println("Hasil bagi: " + hasil);
    }
}
